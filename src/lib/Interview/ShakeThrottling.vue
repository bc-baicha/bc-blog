<template>
  <PageTitle text="防抖和节流" />
  <PageContent>
    <h3>
      当事件触发的过于频繁时，会出现意想不到的错误，接口的重复请求，浏览器丢帧等问题，进而影响用户体验。
    </h3>
    <h4>防抖：将多次重复操作合为一次操作</h4>
    <p>
      原理是维护一个计时器，假如计时器的时间是300ms，如果300ms之内又提交了多次重复操作，那么就会取消之前的计时器，重新设置，直到300ms内没有多次重复操作，最后执行一次操作。
    </p>
    <h4>节流：规定时间内只执行一次操作</h4>
    <p>
      原理也是使用计时器和一个布尔值，提交一次操作之后，执行计时器修改布尔值的状态为执行状态，在规定时间内接收到重复操作将不做任何操作，直到计时器执行结束，再接收下面的操作请求。
    </p>
  </PageContent>
